'''
Побудувати масив А=(a i ), елементи якого задаються формулою:. де x, y – вводяться з клавіатури. Вивести кількість елементів масиву А, які більші за
задане число z.
'''
x=float(input('x='))
y=float(input('y='))
v=[x,y]
n=6
for i in range(4,n+1):
    v.append(v[i-2]+(v[i-1]/2**[i-1])*v[i-3])
print(v)
